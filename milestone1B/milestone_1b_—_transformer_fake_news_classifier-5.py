# Encoding (text → numbers)

def encode(text):
    ids = [vocab.get(t, vocab["<UNK>"]) for t in tokenize(text)]

    # cut if long
    ids = ids[:config.MAX_SEQ_LEN]

    # pad if short
    ids += [vocab["<PAD>"]] * (config.MAX_SEQ_LEN - len(ids))

    return ids

