def clean_news_text(text):
    text = str(text).strip()

    # remove source prefixes
    text = re.sub(r'^[A-Z][A-Z\s]{1,30}(?:,\s*[A-Za-z]+)?\s*\([^)]+\)\s*[-–]\s*', '', text)
    text = re.sub(r'^[A-Z]{2,}(?:\s+[A-Z]{2,})*(?:,\s*[A-Za-z]+)?\s*[-–]\s*', '', text)
    text = re.sub(r'^\((?:Reuters|AP|AFP|BBC|CNN|UPI)\)\s*[-–]?\s*', '', text, flags=re.IGNORECASE)

    return text.strip()


