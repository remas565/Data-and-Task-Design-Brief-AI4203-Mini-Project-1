# Train (one epoch)
# this runs one full pass over training data

def train_one_epoch(model, loader, criterion, optimizer, device):
    model.train()

    total_loss = 0

    for X_batch, y_batch in loader:
        X_batch = X_batch.to(device)
        y_batch = y_batch.to(device)

        optimizer.zero_grad()

        # forward
        outputs = model(X_batch)

        loss = criterion(outputs, y_batch)

        # backward
        loss.backward()
        optimizer.step()

        total_loss += loss.item()

    return total_loss / len(loader)

# Evaluate (validation / test)

def evaluate(model, loader, criterion, device):
    model.eval()

    total_loss = 0
    preds_all = []
    labels_all = []

    with torch.no_grad():
        for X_batch, y_batch in loader:
            X_batch = X_batch.to(device)
            y_batch = y_batch.to(device)

            outputs = model(X_batch)

            total_loss += criterion(outputs, y_batch).item()

            preds = outputs.argmax(1)

            preds_all.extend(preds.cpu().numpy())
            labels_all.extend(y_batch.cpu().numpy())

    avg_loss = total_loss / len(loader)
    acc = accuracy_score(labels_all, preds_all)

    return avg_loss, acc, np.array(preds_all), np.array(labels_all)

# Full Experiment Runner

def run_experiment(dropout, tag, epochs=config.EPOCHS):

    torch.manual_seed(SEED)

    # build model
    model = TransformerClassifier(
        vocab_size=VOCAB_SIZE,
        d_model=config.D_MODEL,
        num_heads=config.NUM_HEADS,
        d_ff=config.D_FF,
        num_classes=config.NUM_CLASSES,
        max_seq_len=config.MAX_SEQ_LEN,
        dropout=dropout
    ).to(config.DEVICE)

    criterion = nn.CrossEntropyLoss()
    optimizer = optim.Adam(model.parameters(), lr=config.LR)

    history = {
        "train_loss": [],
        "val_loss": [],
        "val_acc": []
    }

    print(f"\n{'='*55}")
    print(f"  Experiment : {tag}")
    print(f"  dropout={dropout} | epochs={epochs} | lr={config.LR}")
    print(f"  batch={config.BATCH_SIZE} | max_seq_len={config.MAX_SEQ_LEN}")
    print(f"  d_model={config.D_MODEL} | d_ff={config.D_FF} | heads={config.NUM_HEADS}")
    print(f"{'='*55}")

    # Training loop
    for epoch in range(1, epochs + 1):

        train_loss = train_one_epoch(model, train_loader, criterion, optimizer, config.DEVICE)

        val_loss, val_acc, _, _ = evaluate(model, val_loader, criterion, config.DEVICE)

        history["train_loss"].append(train_loss)
        history["val_loss"].append(val_loss)
        history["val_acc"].append(val_acc)

        print(f"Epoch {epoch}/{epochs} | "
              f"Train Loss: {train_loss:.4f} | "
              f"Val Loss: {val_loss:.4f} | "
              f"Val Acc: {val_acc:.4f}")

    # Final test evaluation
    _, test_acc, test_preds, test_labels = evaluate(model, test_loader, criterion, config.DEVICE)

    print(f"\nFinal Test Accuracy: {test_acc:.4f} ({test_acc*100:.2f}%)")

    print("\nClassification Report:")
    print(classification_report(test_labels, test_preds,
                                target_names=["Fake (0)", "Real (1)"]))

    return model, history, test_acc, test_preds, test_labels

